    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:                                        
        // TODO add your handling code here:
           if(action.equals("NEW")){
            boolean validacion = validar();
            if(validacion){
                int costo_id_pos= cbx_costo.getSelectedItem().toString().indexOf('-');
                int costo_id_val = Integer.parseInt(cbx_costo.getSelectedItem().toString().substring(0, costo_id_pos));
                String ubicacion = txt_ubicacion.getText() ;
                String acomodacion = txt_acomodacion.getText();
                
                model.Habitacion newHabitacion = new model.Habitacion();
                newHabitacion.setUbicacion(ubicacion);
                newHabitacion.setAcomodacion(Integer.parseInt(acomodacion));
                newHabitacion.setCosto_id(costo_id_val);
                
                boolean operacion = dao.insert(newHabitacion);
                if(operacion){
                    parent.refreshTabla();
                    this.dispose();
                }
            }
        }
        if(action.equals("EDIT")){
            boolean validacion = validar();
            if(validacion){
                model.Habitacion hbs = new model.Habitacion(hb.getId());
      
                
                int costo_id_pos= cbx_costo.getSelectedItem().toString().indexOf('-');
                int costo_id_val = Integer.parseInt(cbx_costo.getSelectedItem().toString().substring(0, costo_id_pos));
                String ubicacion = txt_ubicacion.getText() ;
                String acomodacion = txt_acomodacion.getText();
                
                hbs.setUbicacion(ubicacion);
                hbs.setAcomodacion(Integer.parseInt(acomodacion));
                hbs.setCosto_id(costo_id_val);
                boolean operacion = dao.update(hbs);
                if(operacion){
                    parent.refreshTabla();
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_btn_guardarActionPerformed


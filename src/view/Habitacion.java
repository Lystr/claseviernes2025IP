    private void loadHabitacion(){
        if(action == "EDIT"){
            hb = new model.Habitacion();
            hb = (model.Habitacion)dao.getById(id);
            txt_ubicacion.setText(hb.getUbicacion());
            int a = hb.getAcomodacion();
            txt_acomodacion.setText(Integer.toString(a));
            
          
            int i;
            int t;
            String v;
            
            i= 0;
            t = cbx_costo.getItemCount();
            while(i < t){
            cbx_costo.setSelectedIndex(i);
            v = cbx_costo.getSelectedItem().toString();
            if(v.equals(hb.getCosto_d())){
                i = t;
                }
            i= i + 1;
            }
            
            
        }
    }

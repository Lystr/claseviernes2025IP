    private boolean validar() {
        
        String ubicacion = txt_ubicacion.getText();
        String acomodacion = txt_acomodacion.getText(); 
         if(ubicacion.equals("") || ubicacion.equals(null)){
            JOptionPane.showMessageDialog(parent, "Debe Ingresar la ubicacion", "ERROR DE VALIDACIÓN", JOptionPane.ERROR_MESSAGE);
            return false;
        }
         if(acomodacion.equals("") || acomodacion.equals(null)){
            JOptionPane.showMessageDialog(parent, "Debe ingresar la acomodacion", "ERROR DE VALIDACIÓN", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    } 

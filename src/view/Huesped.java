    private void btn_guardaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardaActionPerformed
        // TODO add your handling code here:
           if(action.equals("NEW")){
            boolean validacion = validar();
            if(validacion){
                model.Huesped newHuesped = new model.Huesped();
                newHuesped.setPrimer_nombre(txt_primer_nombre.getText());
                newHuesped.setPrimer_apellido(txt_primer_apellido.getText());
                newHuesped.setEdad(Integer.parseInt(txt_edad.getText()));
                boolean operacion = dao.insert(newHuesped);
                if(operacion){
                    parent.refreshTabla();
                    this.dispose();
                }
            }
        }
        if(action.equals("EDIT")){
            boolean validacion = validar();
            if(validacion){
                hp.setPrimer_nombre(txt_primer_nombre.getText());
                hp.setPrimer_apellido(txt_primer_apellido.getText());
                hp.setEdad(Integer.parseInt(txt_edad.getText()));
                boolean operacion = dao.update(hp);
                if(operacion){
                    parent.refreshTabla();
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_btn_guardaActionPerformed

    
    

    private boolean validar() {
         if(txt_primer_nombre.getText().equals("") || txt_primer_nombre.getText().equals(null)){
            JOptionPane.showMessageDialog(parent, "Debe ingresar un nombre", "ERROR DE VALIDACIÓN", JOptionPane.ERROR_MESSAGE);
            return false;
        }
         if(txt_primer_apellido.getText().equals("") || txt_primer_apellido.getText().equals(null)){
            JOptionPane.showMessageDialog(parent, "Debe ingresar un nombre", "ERROR DE VALIDACIÓN", JOptionPane.ERROR_MESSAGE);
            return false;
        }
         if(txt_edad.getText().equals("") || txt_edad.getText().equals(null)){
            JOptionPane.showMessageDialog(parent, "Debe ingresar un nombre", "ERROR DE VALIDACIÓN", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }    

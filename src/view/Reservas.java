    private void cargarDatosReservas(){
         modeloTabla.setRowCount(0);
         modeloTabla.setRowCount(0);
        List<Object> reservas = reservasDAO.getAll();
        for(Object reserva_uncast : reservas){
            ReservasDTO reserva = (ReservasDTO) reserva_uncast;
            modeloTabla.addRow(new Object[]{reserva.getReserva_id(), reserva.getFecha_reserva(),reserva.getFecha_salida(),reserva.getHabitacion(),reserva.getHuesped(),reserva.getRegistro()});
        }
        
    }

